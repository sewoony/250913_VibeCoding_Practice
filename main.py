# 유형이 선택되면 정보 표시
if selected_mbti:
    mbti_info = mbti_data[selected_mbti]

    # MBTI 설명 및 효과 추가
    st.balloons()
    st.header(f"{mbti_info['이름']} {mbti_info['이모지']}")
    st.subheader(f"🗣️ 한마디: {mbti_info['설명']}")

    st.markdown("---")

    # 공부법 추천 효과
    with st.spinner("최고의 공부법을 찾고 있어요... 🕵️‍♀️"):
        time.sleep(1.5)
        st.success("✨ 찾았습니다! 당신을 위한 추천 공부법이에요! ✨")

    # 공부법 추천 목록
    for i, recommendation in enumerate(mbti_info['추천']):
        st.info(f"👉 **추천 공부법 {i + 1}**\n\n{recommendation}")

    # 추가 재미 효과
    if st.button('다른 공부법도 궁금해! 👀'):
        st.snow()
        st.info("사실... 모든 공부법은 당신의 노력에 달려있어요! 힘내세요! 💪😉")

    st.markdown("---")
